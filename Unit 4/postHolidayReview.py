# Data Types - Level 1: Most basic building block of code.
# (numbers, letters, true or false)

# Operators - Level 2: The ability to manipulate and do things
# with data types. (math, comparisions, assignment, etc.)

# Functions - Level 3: Taking the first two concepts and organizing 
# these operations and data types into instructions.

# Conditional Statements - Level 3a: Being able to add more control 
# on our function instructions.